/*
Show the following day of the week
*/
const day = prompt("What day is it?")

if (day == "Sunday") {
    alert("Next day is Monday")
} else if (day == "Monday") {
    alert("Next day is Tuesday")
} else if (day == "Tuesday") {
    alert("Next day is Wednesday")
} else if (day == "Wednesday") {
    alert("Next day is Thursday")
} else if (day == "Thursday") {
    alert("Next day is Friday")
} else if (day == "Friday") {
    alert("Next day is Saturday")
} else {
    alert("Next day is Sunday")
}